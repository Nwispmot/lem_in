#include "libft.h"

void	ft_putnbr(int n)
{
	int d;

	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = n * (-1);
	}
	d = 0;
	if (n > 0)
	{
		d = n % 10;
		if (n / 10 > 0)
			ft_putnbr(n / 10);
	}
	ft_putchar(d + '0');
}
