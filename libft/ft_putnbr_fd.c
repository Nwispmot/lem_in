#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int d;

	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = n * (-1);
	}
	d = 0;
	if (n > 0)
	{
		d = n % 10;
		if (n / 10 > 0)
			ft_putnbr_fd(n / 10, fd);
	}
	ft_putchar_fd(d + '0', fd);
}
